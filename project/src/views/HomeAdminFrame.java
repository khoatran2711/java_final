    private void LoadNV(ArrayList<NhanVien> nvArr) {
        this.nvArr = nvArr;
        vNDungNV = new Vector();
        for (NhanVien nv : nvArr) {
            NhapNV(nv);
        }

        dtmNV.setDataVector(vNDungNV, vTieuDeNV);
        tableNV.setModel(dtmNV);

    }

    private void LoadDoanhSo(boolean hasDate) {
        ArrayList<Bill> billArr;
        if (hasDate) {
            billArr = qlHoaDon.GetHoaDon(dtStart.getDate(), dtEnd.getDate());
        } else {
            billArr = qlHoaDon.GetAllHoaDon();
        }
        TieuDeDS();
        vNDungDS = new Vector();
        for (Bill bill : billArr) {
            NhapDS(bill);
        }

        dtmDS.setDataVector(vNDungDS, vTieuDeDS);
        tableDS.setModel(dtmDS);
    }

